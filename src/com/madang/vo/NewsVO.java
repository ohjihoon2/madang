package com.madang.vo;

public class NewsVO {
	String nw_code, nw_title, nw_date, nw_press, nw_url;

	public String getNw_code() {
		return nw_code;
	}

	public void setNw_code(String nw_code) {
		this.nw_code = nw_code;
	}

	public String getNw_title() {
		return nw_title;
	}

	public void setNw_title(String nw_title) {
		this.nw_title = nw_title;
	}

	public String getNw_date() {
		return nw_date;
	}

	public void setNw_date(String nw_date) {
		this.nw_date = nw_date;
	}

	public String getNw_press() {
		return nw_press;
	}

	public void setNw_press(String nw_press) {
		this.nw_press = nw_press;
	}

	public String getNw_url() {
		return nw_url;
	}

	public void setNw_url(String nw_url) {
		this.nw_url = nw_url;
	}
	
	
}
