package com.madang.vo;

public class NoticeVO {
	String nt_code, nt_title, nt_contents, nt_file, nt_sfile, nt_date;
	int nt_hits;
	public String getNt_code() {
		return nt_code;
	}
	public void setNt_code(String nt_code) {
		this.nt_code = nt_code;
	}
	public String getNt_title() {
		return nt_title;
	}
	public void setNt_title(String nt_title) {
		this.nt_title = nt_title;
	}
	public String getNt_contents() {
		return nt_contents;
	}
	public void setNt_contents(String nt_contents) {
		this.nt_contents = nt_contents;
	}
	public String getNt_file() {
		return nt_file;
	}
	public void setNt_file(String nt_file) {
		this.nt_file = nt_file;
	}
	public String getNt_sfile() {
		return nt_sfile;
	}
	public void setNt_sfile(String nt_sfile) {
		this.nt_sfile = nt_sfile;
	}
	public String getNt_date() {
		return nt_date;
	}
	public void setNt_date(String nt_date) {
		this.nt_date = nt_date;
	}
	public int getNt_hits() {
		return nt_hits;
	}
	public void setNt_hits(int nt_hits) {
		this.nt_hits = nt_hits;
	}
	
	
}
